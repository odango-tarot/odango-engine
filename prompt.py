def build_user_message(
    client_name: str,
    consultation: str,
    consultation_text: str,
    method_label: str,
    raw_data: str,
    memo: str,
    char_count: int,
    mode: str,
) -> str:
    mode_label = "出目から鑑定文を生成" if mode == "generate" else "下書きをおだんご節にリライト"

    message = f"""# タスク
{mode_label}してください。

# クライアント情報
- お名前：{client_name}さん
- 占術：{method_label}
- ご相談ジャンル：{consultation if consultation else "（記載なし）"}
"""

    if consultation_text:
        message += f"""
# クライアントの依頼文章（原文）
{consultation_text}
"""

    if raw_data:
        message += f"""
# 出目
{raw_data}
"""

    if memo:
        message += f"""
# 占い師の補足メモ
{memo}
"""

    message += f"""
# 出力指定
- 文字数：{char_count}字。これは目安ではなく必達目標です。{char_count}字に到達するまで書き続けてください。不足する場合は各カードの解釈・クライアントへの語りかけを丁寧に肉付けして補うこと。
- 上記の「おだんご文体定義」に完全に従って執筆してください
- クライアント名「{client_name}」を正しく使用してください
"""

    return message


def get_mashup_system_prompt(method_key1: str, method_key2: str, person_setting: dict = None) -> str:
    method_label1 = FORTUNE_METHODS.get(method_key1, "占い")
    method_label2 = FORTUNE_METHODS.get(method_key2, "占い")
    base = get_system_prompt(method_key1, person_setting)
    mashup_instruction = f"""
# マッシュアップ追加指示
{method_label1}に加えて{method_label2}の出目も組み合わせて鑑定します。
- 2つを別々に解説せず、自然に絡み合わせて1本にまとめる
- どちらかが「補強」、もう一方が「具体的メッセージ」という役割分担を自然に作る
"""
    return base + mashup_instruction


