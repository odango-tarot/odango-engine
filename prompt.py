def build_lenormand_person_setting(person_setting: dict) -> str:
    """ルノルマン人物カード設定をプロンプト文字列に変換する"""
    if not person_setting:
        return ""

    lines = ["\n## 今回の人物カード設定（最重要）"]

    client_gender = person_setting.get("client_gender", "女性")
    lady_role = person_setting.get("lady_role", "依頼者本人")
    knight_role = person_setting.get("knight_role", "恋人・配偶者")
    lady_custom = person_setting.get("lady_custom", "")
    knight_custom = person_setting.get("knight_custom", "")

    lines.append(f"- 依頼者の性別：{client_gender}")

    lady_desc = lady_custom if lady_role == "その他" and lady_custom else lady_role
    lines.append(f"- 淑女（29番）＝ {lady_desc}")

    knight_desc = knight_custom if knight_role == "その他" and knight_custom else knight_role
    lines.append(f"- 紳士（28番）＝ {knight_desc}")

    if client_gender == "女性":
        lines.append("- 依頼者を表すカード：淑女（29番）")
        lines.append("- 鑑定文冒頭の見出しは【淑女の周辺】とし、淑女を起点にタブローを読む")
    else:
        lines.append("- 依頼者を表すカード：紳士（28番）")
        lines.append("- 鑑定文冒頭の見出しは【紳士の周辺】とし、紳士を起点にタブローを読む")

    lines.append("- 上記の人物設定を鑑定文全体を通じて一貫して使用すること")

    return "\n".join(lines)


